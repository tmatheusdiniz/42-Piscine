#include <unistd.h>

int	main(int argc, char **str)
{
	int i;
	int j;

	i = 0;
	if (argc == 2)
	{
		while(str[1][i])
		{
			j = 0;
			if (str[1][i] >= 'A' && str[1][i] <= 'Z')
			j = str[1][i] - 'A';
			if (str[1][i] >= 'a' && str[1][i] <= 'z')
			j = str[1][i] - 'a';

			while (j >= 0)
			{
				write(1, &str[1][i], 1);
				j --;
			}
			i ++;
		}
	}
	write (1, "\n", 1);
}
